import xml.etree.ElementTree as ET
from pathlib import Path
from typing import Dict, Tuple


# ----------------------------------
# Main wrapper 
# ----------------------------------

# Orchestration runtime interface
## Global entrypoint coordination function
def verify_xml_dependencies(xml_path: Path, maboss_models_map: Dict[str, Dict[str, Path]]) -> None:
    """
    Coordinates modular validation checks to ensure project configuration integrity across models and disk targets.

    :param xml_path: Path to the simulation setup XML file.
    :type xml_path: Path
    :param maboss_models_map: Paired model structure dictionary generated by discovery utilities.
    :type maboss_models_map: Dict[str, Dict[str, Path]]
    """
    # Parse document structure layout
    tree = ET.parse(xml_path)
    root = tree.getroot()
    intracellular_found = False

    # Process all structural cell definitions in sequence
    ## Core mapping validation loop
    for cell_def in root.findall(".//cell_definition"):
        cell_name = cell_def.attrib.get("name", "Unknown_Cell")
        intracellular = cell_def.find(".//phenotype/intracellular")
        
        if intracellular is not None and intracellular.attrib.get("type") == "maboss":
            intracellular_found = True
            
            ### Execute atomic validations using helper routines
            bnd_file, cfg_file = _validate_intracellular_elements(cell_name, intracellular)
            model_prefix = _validate_prefix_match(cell_name, bnd_file, cfg_file)
            _validate_map_and_disk_presence(cell_name, model_prefix, bnd_file, cfg_file, maboss_models_map)
            
            print(f" -> Cell '{cell_name}' structural binding verified with MaBoSS model: '{model_prefix}'")

    # Handle empty multi-scale model bounds faults
    if not intracellular_found:
        raise ValueError(f"Validation Error: The configuration file {xml_path} contains no cell definitions with an intracellular MaBoSS engine.")

    # Execute spatial architecture dependencies verification
    _validate_external_spatial_files(root, xml_path)

